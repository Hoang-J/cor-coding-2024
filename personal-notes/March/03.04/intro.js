// let is a keyword that initializes the variable name. A variable
// initializes with let can be modified multiple times

let fullname = "Dre Taylor";
fullname = "City of Refuge";

// A variable initialized with const cannot be changed
const birthyear = 1900;


let user = {
    email: "dre@test.com",
    password: "password123",
    address: "123 Apple Lane",
    city: "Atlanta"
}

list = [43, 543, 532, 5];

// Data can be printed to the terminal using the function console.log()
// When a function is called, any variables passed into the function are
// called arguments

console.log(fullname);
console.log(birthyear);
console.log(user);
console.log(list);


let age = "45";
// Putting 'age =' will store the age value above
age = age + 1; 

// Camelcase is when we start a variable with a lowercase, and each new word is
// Uppercase
let firstName = 'Dre'

// Snake case uses all lowercase and separates words by _ (underscore)
let last_name = 'Taylor'
// Concatentation 
completeName = firstName + " " + last_name;
console.log(completeName);